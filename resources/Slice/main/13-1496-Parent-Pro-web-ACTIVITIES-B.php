                <li><strong>3)</strong> Fold down the extra two inches of paper on each diagonal and secure with tape.</li>
                <li><strong>4)</strong> Tape the paper portion of the kite to the cross. </li>
                <li><strong>5)</strong> Tie a long piece of string to where the bars cross, leaving the end attached to the spool. </li>
